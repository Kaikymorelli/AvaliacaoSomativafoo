package AvaliativaSomativa;

public class Desafio4 {
	
	private String nome;
	private int id;
	
	public Desafio4 () {
		
	}
	
	public Desafio4(String nome, int id) {
		super();
		this.nome = nome;
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
	
	

}
